package assertj.sp1ta4le2ex1;

public class TwoObjects {
    private int value;

    public TwoObjects(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) return true;
        if (obj == null || getClass() != obj.getClass()) return false;
        TwoObjects that = (TwoObjects) obj;
        return value == that.value;
    }

    @Override
    public int hashCode() {
        return Integer.hashCode(value);
    }

    @Override
    public String toString() {
        return "TwoObjects{value=" + value + "}";
    }
}
